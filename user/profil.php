                            <h1 class="font-bold">Pembentukan Desa</h1>
                        </div>
                        <div>
                            <p class="py-3 text-justify">
                                Kalurahan Tamantirto terbentuk pada tahun 1946. Nama Tamantirto merupakan pemberian dari Sri Sultan Hamengku Buwono IX. Kalurahan Tamantirto merupakan Kalurahan gabungan dari 3 (Tiga) wilayah yaitu Kalurahan Ngebel Lama, Kalurahan Kasihan Lama, dan Kalurahan Sumberan Lama. Penggabungan wilayah menjadi satu kesatuan ini terjadi pada tanggal 15 Suro 1878 atau tepatnya 10 Desember 1946/16 Muharam 1366 H dengan Suryo Sengkolo Rasa Wening Ambuko Projo.<br>
                                <br>
                                Sebelum terjadinya penggabungan 3 (Tiga) wilayah tersebut, tokoh Lurah dari masing-masing Kalurahan lama adalah Bapak Ranu Dimejo sebagai Lurah Ngebel, Bapak Parto Sentono, Bapak Cokro Atmojo, Bapak Pawiro Diharjo secara berurutan sebagai Lurah Sumberan, dan Bapak Suto Rejo, Bapak Darmo Suwito, dan Bapak Wiryo Sukarto secara berurutan sebagai Lurah Kasihan.<br>
                                <br>
                                Setelah penggabungan menjadi Kalurahan Tamantirto, struktur awal menjadi Bapak Pawiro Diharjo sebagai Lurah, Bapak Mocheni sebagai Carik, Bapak Muh. Usuf dari Mranggen sebagai Jogo Boyo, Bapak Sastro Dimejo dari Kembang sebagai Sosial, Bapak Darmo Suwito dari Ngrame sebagai Ulu-Ulu, dan Bapak Pujo Suwarsono dari Rukeman sebagai Modin.<br>
                                <br>
                                Dari struktur baru yang di isi oleh 6 (Enam) tokoh tersebut yang kemudian menjadi cikal bakal Kalurahan Tamantirto dengan kantor kalurahan yang sempat mengalami perpindahan tempat 3 (Tiga) kali. Pertama ada di rumah Bapak Pawiro Diharjo daerah Selokambang Padukuhan Gatak. Kedua, berpindah ke rumah Bapak Muecheni di Sumberan Padukuhan Jadan. Ketiga, berpindah di rumah Bapak Dwijo Martono di Tegalrejo sebelum kemudian berpindah dan menetap di Balai Kalurahan Tamantirto sampai hari ini.<br>
                                <br>
                                Mengulas lebih jauh tentang Kalurahan Tamantirto, istilah Alas Ketoyan identik dengan wilayah ini. Alas bermakna hutan, sedangkan Ketoyan bermakna Air. Istilah tersebut menandakan bahwa wilayah Kalurahan Tamantirto merupakan wilayah yang kaya dengan sumber air. Pada masa kini, istilah tersebut terbukti dengan adanya beberapa titik mata air di wilayah Kasihan, Brajan, Godekan, dan sungai besar Mbedok yang masih aktif sampai hari ini.<br>
